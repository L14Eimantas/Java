    private void piesinys1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_piesinys1KeyReleased
        // TODO add your handling code here:
        switch (evt.getKeyCode()) {
            case 38:
                l.getTaskai().get(0).eik(Kryptys.VIRSUS);
                kl.siustiZinute("eik:"+0);
                break;
            case 39:
                l.getTaskai().get(0).eik(Kryptys.DESINE);
                kl.siustiZinute("eik:"+1);
                break;
            case 40:
                l.getTaskai().get(0).eik(Kryptys.APACIA);
                kl.siustiZinute("eik:"+2);
                break;
            case 37:
                l.getTaskai().get(0).eik(Kryptys.KAIRE);
                kl.siustiZinute("eik:"+3);
                break;
        }
        /////////////////////////////////////       
        this.repaint();
    }//GEN-LAST:event_piesinys1KeyReleased

