    private void RasymasKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_RasymasKeyReleased
        if (evt.getKeyCode()==10 && kl!=null){
            String txt=Rasymas.getText();
            Rasymas.setText("");
            try{
            if (KlientuSarasas.getSelectedValue().equals("Visi")){
            kl.siustiZinute("v:"+txt);
            }else{
                kl.siustiZinute("pm:"+KlientuSarasas.getSelectedValue()+":"+txt);
            }
            }
            catch (Exception ex){
               kl.siustiZinute("v:"+txt);}
            isvestiAtsaka(txt);
            piesinys1.grabFocus();
        }
    }//GEN-LAST:event_RasymasKeyReleased

