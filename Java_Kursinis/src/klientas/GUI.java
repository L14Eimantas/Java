    public void isvestiAtsaka(String txt){
        Chatas.append(txt+"\n");
        Chatas.setCaretPosition(Chatas.getDocument().getLength());// textarea rodytu i apacia
    }
    public void pridetiKlienta(String n){
        klientu_nikai.add(n);
        KlientuSarasas.setModel(new javax.swing.AbstractListModel(){
            String[] strings =klientu_nikai.toArray(new String[klientu_nikai.size()]);
            @Override
            public int getSize() {return strings.length;}
            @Override
            public Object getElementAt (int i) {return strings[i];}
        });
    }
    
    public void pasalintiKlienta(String n){
        klientu_nikai.remove(n);
        KlientuSarasas.setModel(new javax.swing.AbstractListModel() {
            String[] strings =klientu_nikai.toArray(new String[klientu_nikai.size()]);
            @Override
            public int getSize() {
              return strings.length;
            }
            @Override
            public Object getElementAt(int i) {
               return strings[i];
            }
        });
    }
 
